def get_assembly_info(assembly_key: str) -> dict:
    """Get assembly information by key."""
    return BUGGY_ASSEMBLIES.get(assembly_key, {})


def get_repair_steps(assembly_key: str) -> list:
    """Get step-by-step repair instructions for an assembly."""
    assembly = BUGGY_ASSEMBLIES.get(assembly_key, {})
    if not assembly:
        return []

    steps = []
    steps.append(f"To repair/build the {assembly['name']}:")
    steps.append("")
    steps.append("Required Parts:")

    for i, comp in enumerate(assembly.get("components", []), 1):
        steps.append(f"  {i}. Element #{comp['number']}: {comp['name']}")

    steps.append("")
    steps.append("Assembly Steps:")

    if "wheel" in assembly_key:
        steps.append("  1. Take the Wheel rim (white part)")
        steps.append("  2. Insert the Technic Bush (red) into the center")
        steps.append("  3. Add the Technic Pin half (grey) through the bush")
        steps.append("  4. Press the Tire (black) onto the rim")
        steps.append(f"  5. Attach the complete wheel to the "
                     f"{assembly['location']}")
    elif "bumper" in assembly_key:
        steps.append("  1. Gather all lime/green slope pieces")
        steps.append("  2. Attach slopes to the front chassis studs")
        steps.append("  3. Ensure grille pieces face forward")
    elif "spoiler" in assembly_key or "visor" in assembly_key:
        steps.append("  1. Take the two wedge plates (green)")
        steps.append("  2. Connect them at an angle to form the wing")
        steps.append("  3. Attach to the rear mounting points")
    elif "axle" in assembly_key:
        steps.append("  1. Insert the Technic Axle through the chassis holes")
        steps.append("  2. Add the Pin Connector plates for support")
        steps.append("  3. Attach Liftarms to secure the axle")

    return steps


def get_stock_image_path(view_angle: str) -> str:
    """Get the stock image path for a view angle."""
    filename = STOCK_IMAGES.get(view_angle, "13.jpeg")  # Default to isometric
    return f"src/stock/{filename}"


def format_missing_assembly_report(missing_assemblies: list) -> str:
    """Format a report of missing assemblies with repair information."""
    report = []

    for assembly_key in missing_assemblies:
        assembly = BUGGY_ASSEMBLIES.get(assembly_key, {})
        if not assembly:
            continue

        report.append(f"\n## Missing: {assembly['name']}")
        report.append(f"Location: {assembly['location']}")
        report.append(f"Importance: {assembly['importance'].upper()}")
        report.append("")
        report.append("### Parts Required:")

        for comp in assembly.get("components", []):
            report.append(f"- Element #{comp['number']}: {comp['name']}")

        report.append("")
        report.append("### Repair Steps:")
        steps = get_repair_steps(assembly_key)
        for step in steps[steps.index("Assembly Steps:") + 1:]:
            report.append(step)

    return "\n".join(report)
